// type Casting
//Type casting, also known as type conversion, is a fundamental concept in programming that involves changing the data type of a value or an expression from one data type to another.

#include <iostream>
using namespace std;

int main(){
	int a = 'a'; 
	cout << a << endl; // 97

	char ch = 98;
	cout << ch << endl;

	return 0;
}